# lay out a hexagonal grid of concentric circles of radius 1 (so 2 units between centres)

$t = 1.732; # inter-row distance (2 * sin 60)

$positions = [
	[0,0],
	# first ring
	[-1, $t],
	[1, $t],
	[2, 0],
	[1, -$t],
	[-1, -$t],
	[-2,0],
	# second ring
	[-2, 2*$t],
	[0, 2*$t],
	[2, 2*$t],
	[3, $t],
	[4, 0],
	[3, -$t],
	[2, -2*$t],
	[0, -2*$t],
	[-2, -2*$t],
	[-3, -$t],
	[-4, 0],
	[-3, $t],
	# outliers
	[4, 2*$t],
	[5, $t],
	[6, 0],
	[-4, 2*$t],
	[-5, $t],
	[-6, 0],
	# final outlier
	[-1, 3*$t]
];

# Pre-calculate the distances between all the positions
# - assume that n>=m, so we can skip half the calculations

$distances = [];

print "precalculating...\n";

foreach my $n (0..25) {
	foreach my $m (0..$n) {
		$distance = 0;
		if ($n != $m) {
			$dx = $positions->[$n][0] - $positions->[$m][0];
			$dy = $positions->[$n][1] - $positions->[$m][1];
			$distance = sprintf("%.3f",sqrt($dx*$dx + $dy*$dy));
		}
		$distances[$n][$m] = $distance;
	#	print "From $n to $m is $distance\n";
	}
}
print "done\n";

# Finally, figure out our layout, starting with a random order of a-z.
# character position in the string equates to the position list above

$layout = join('',shuffle('a'..'z'));

$current_energy = score($layout);
printf("%-4.4f %s\n\n", $current_energy, $layout);

$generations = 0;

$temperature = 100000;
$cooling_rate = 0.0001;

$mutate_with_no_improvement = 0;

do {
	$last_layout = $layout;
	$generations++;
	
	$y = 0; # at one point the number of mutations also depended on temperature
		
	for (0..$y) {
		
		# mutate...
		$from = int(rand(26));
		$to = int(rand(26));
		
		if ($from != $to) {
			$tmp = substr($layout, $from, 1);
			substr($layout, $from, 1) = substr($layout, $to, 1);
			substr($layout, $to, 1) = $tmp;
		}
	}
	
	$new_energy = score($layout);

	$accept = acceptance($current_energy, $new_energy, $temperature);	
	
	if ($accept < rand()) {
		$layout = $last_layout;
		$mutate_with_no_improvement++;
	} else {
		printf("\n<%f> %-4.4f %s [%d]{%d} %f->%f\n", $temperature, $new_energy, $layout, 
				$generations, $mutate_with_no_improvement, 
				$current_energy, $new_energy);
		
		$current_energy = $new_energy;
		$mutate_with_no_improvement = 0;
		print_layout($layout);	
	}

	$temperature = $temperature * (1 - $cooling_rate);
	
} while($temperature > 1);

# At this point, we have reached some kind of solution

