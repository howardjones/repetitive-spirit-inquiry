@headings = split(/\s+/,<DATA>);

$weight_ceiling = 22038615;

# Read in the word list, iterate through all the letter-pairs in each word 
# and accumulate a count of the occurence of each pair, weighted by how often
# the word appears in the corpus (e.g. 'ee' more than 'ex')

while(<DATA>) {
	($word, $pos, $freq) = split(/\s+/);

	# spirits don't use punctuation
	$word =~ tr/a-z//cd;
	
	$l = length($word);
	
	next if $pos eq 'x'; # skip particles
	next if $l == 1; # one letter words don't have neighbours
	
	$weight = ($freq/$weight_ceiling) ** 1.5;
	
	for $i (0..($l-2)) {
		$transition = substr($word, $i, 2);
		# print "$transition ";
		$transitions{$transition} += $weight;
	}	
}

# $i=0;
# @pairs = sort {$transitions{$b} <=> $transitions{$a}} keys %transitions;
