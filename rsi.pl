# Decide the probability of accepting the new configuration,
# based on the old energy, the new energy, and the current temperature
sub acceptance {
	my ($energy1, $energy2, $temperature) = @_;
	
	# if the energy is lower, always accept
	if ($energy2 < $energy1) {
		return 1;
	} else {
		# if the energy is higher, maybe accept anyway, depending on temperature,
		# and how much of a backwards step it would be
		return exp( ($energy1 - $energy2) / $temperature );
	}
}

# Calculate the "energy" of a given layout. This is the combination of the weights
# of all the letter-pairs, and their distance from each other in the current layout
# the lowest energy is where the most common letters are closest to each other most
sub score {
	my $total = 0;
	my $input = shift;
	
	foreach my $t (keys %transitions) {
		my $from = substr($t,0,1);
		my $to = substr($t,1,1);
	
		my $start = index($input, $from);
		my $finish = index($input, $to);
	
		$distance = hex_distance($start, $finish);
		
		# print "  from $from to $to is $distance steps\n";
		
		$total += $distance * $transitions{$t};
	}
	
	return sprintf("%.5f",$total);
}

# calculate the distance between two letters in the current layout
sub hex_distance {
	my ($i1, $i2) = @_;
	
	if ($i1 >= $i2) {
		return $distances[$i1][$i2];
	} else {
		return $distances[$i2][$i1];
	}	
}

# print a pretty ASCII-art picture of the current layout
sub print_layout {

	my ($input) = shift;
	
	my @list = split(//, $input);
	
	printf("      %s      \n", @list[25]);
	printf("  %s %s %s %s %s  \n", @list[22,7,8,9,19]);
	printf(" %s %s %s %s %s %s \n", @list[23,18,1,2,10,20]);
	printf("%s %s %s %s %s %s %s\n", @list[24,17,6,0,3,11,21]);
	printf("   %s %s %s %s   \n", @list[16,5,4,12]);
	printf("    %s %s %s    \n", @list[15,14,13]);

}

sub svg_layout {
	my ($layout) = shift;
	my ($energy) = shift;
	
	my $scale = 32;
	my $offset = 256;
	my $fill;
	
	my $fontsize = $scale * 0.8;
	
	my $output = "<svg width='512' height='512'>\n";
	
	
	foreach my $i (0..25) {
		$fill = "crimson";
		$fill = "deeppink" if $i>0;
		$fill = "mediumorchid" if $i>6;
		$fill = "mediumpurple" if $i>18;
		
		$output .= sprintf('<circle cx="%f" cy="%f" r="%f" stroke="black" stroke-width="3" fill="%s" />', 
			$positions->[$i][0]*$scale*1.1 + $offset, 
			$offset - $positions->[$i][1]*$scale*1.1,
			$scale,
			$fill
			);
		
		$output .= sprintf('<text x="%f" y="%f" fill="black" font-family="Gill Sans" font-size="%d" >%s</text>', 
			$positions->[$i][0]*$scale*1.1 + $offset - $fontsize*0.3, 
			$offset - $positions->[$i][1]*$scale*1.1 + $fontsize*0.3,
			$fontsize,
			uc substr($layout, $i, 1)
			);
		
		$output .= "\n";
	}
	$output .= sprintf('<text x="10" y="20" fill="silver">Energy: %f</text>', $energy);
	$output .= "</svg>\n";
	
	return $output;	
}

